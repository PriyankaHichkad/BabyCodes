//Q- WAP to calculate and display the sum of the first ten natural numbers.
public class sum10natural {
    public static void main(String[] args) {
        int s=0, i=0;
        for( ;i<=10; i++)
            s=s+i;
        System.out.println("i=" +i);
        System.out.println("Sum=" +s);
    }
}

//Q- WAP to accept three numbers and find the greatest number among them.
public class usingIf {
    public static void main(String[] args, int a, int b, int c) {
        if(a>b && a>=c)
    {
        System.out.println(a);
    }
    else if(b>a && b>c)
    {
        System.out.println(b);
    }     
    else if(c>a && c>b)
    {
        System.out.println(c);
    }
    }
}
